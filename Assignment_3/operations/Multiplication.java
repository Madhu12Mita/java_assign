package operations;

public class Multiplication 
{
    public String multiply(int a, int b)
    {
        return String.valueOf(a*b);
    }    
    public String multiply(float a, float b)
    {
        return String.valueOf(a*b);
    }  
    public String multiply(long a, long b)
    {
        return String.valueOf(a*b);
    }  
    public String multiple(double a, double b)
    {
        return String.valueOf(a*b);
    }
    public String multiple(long a,double b)
    {
        return String .valueOf(a*b);
    }
}

